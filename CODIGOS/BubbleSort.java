public class BubbleSort {

    public void bubbleSort(int[] a) {
        if (a == null) {
            throw new NullPointerException("The array doesn't exist.");
        }
        for (int i = 0; i < a.length - 1; i++) {
            for (int j = 0; j < a.length - i - 1; j++) {
                if (a[j] > a[j + 1]) {
                    int temp = a[j];
                    a[j] = a[j + 1];
                    a[j + 1] = temp;
                }
            }
        }
    }
}
